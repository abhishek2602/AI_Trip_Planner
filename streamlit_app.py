import streamlit as st
import datetime
import requests

import sys

BASE_URL = "http://127.0.0.1:8000"  # Backend endpoint

st.set_page_config(
    page_title="Travel Planner Agentic Application",
    page_icon="✈️",
    layout="centered",
    initial_sidebar_state="expanded",
)


st.title("Travel Planner Agentic Application")

# Initialise chat history
if "messages" not in st.session_state:
    st.session_state.messages = []

# Display chat history
st.header(
    "How can I help you in planning a trip? Let me know where you want to visit. "
)

# Chat input box at bottom
with st.form(key="query_form", clear_on_submit=True):
    user_input = st.text_input(
        "User Input:", placeholder="e.g. Plan a trip to Goa for 5 days"
    )
    submit_button = st.form_submit_button("Send")


if submit_button and user_input.strip():
    try:
        # Show user message
        # Show thinking spinner while backend processes
        with st.spinner("Agent is thinking..."):
            payload = {"question": user_input}
            response = requests.post(f"{BASE_URL}/query", json=payload)

        if response.status_code == 200:
            answer = response.json().get("answer", "No answer returned")
            markdown_content = f"""# AI Travel Plan
            
            # **Generated:** {datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')}
            # **Created by:** Genailytics's Travel Agent
            
            ---
            
            {answer}
            
            ---
            *This travel plan was generated by AI. Please verify all information, especially prices, operating hours, and travel requirements before your trip.*
            """
            st.markdown(markdown_content, unsafe_allow_html=True)

        else:
            st.error("Agent failed to respond: " + response.text)

    except Exception as e:
        raise f"The response failed due to {e}"
